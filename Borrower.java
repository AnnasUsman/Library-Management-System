    //function to search book
    private void btn_searchMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_searchMousePressed
        // TODO add your handling code here:
        ResultSet rs;
        String text = txt_bx.getText();
        if (cmb_search.getSelectedItem().equals("book")){
            if(cmb_type.getSelectedItem().equals("By Title"))
            {
                rs = Login.searchBook("title",text,1); 
            }
            else if (cmb_type.getSelectedItem().equals("By author"))
            {
                rs = Login.searchBook("author",text,1); 
            }
            else
            {
                rs = Login.searchBook("subject",text,1); 
            }
        }
        else{
            if(cmb_type.getSelectedItem().equals("By Title"))
            {
                rs = Login.searchBook("title",text,0);
            }
            else if (cmb_type.getSelectedItem().equals("By author"))
            {
                rs = Login.searchBook("author",text,0);
            }
            else
            {
                rs = Login.searchBook("subject",text,0);
            }
        }
        try
        {
            rs.next();
            if (rs.getRow()==0)
            {
                JOptionPane.showMessageDialog(null, "There are currently no items with that name");
            }
            else{
                DefaultTableModel model = (DefaultTableModel) search_table.getModel();
                int rows = search_table.getRowCount();
                for (int i=0 ; i<rows ; i++){
                    model.removeRow(0);
                }
                for(int i=0;rs.getRow()!=-1;i++)
                {
                    Object id = rs.getObject("ISBN");
                    Object title=rs.getObject("title");
                    Object author= rs.getObject("author");
                    Object subject= rs.getObject("subject");
                    Object[] row={id,title,author,subject};
                    model.addRow(row);
                    rs.next();
                }
            }
        }
        catch (Exception e)
        {
            System.out.println(e);
        }
    }//GEN-LAST:event_btn_searchMousePressed

