    //function to view the history
    public void history(int id)
    {
        
        DefaultTableModel model = (DefaultTableModel) user_table.getModel();
        int rows = user_table.getRowCount();
        for (int i=0 ; i<rows ; i++){
            model.removeRow(0);
        }
        
        ResultSet rs = Login.userHistory(id);
        try{
            rs.next();
            int size = rs.getRow();
            if(size>0)
            {
                for(int i=0;rs.getRow()!=-1;i++)
                {
                    int item_id = rs.getInt("BOOK");
                    String name="";
                    Object issueDate= rs.getObject("ISSUE_DATE");
                    Object returnDate= rs.getObject("RETURN_DATE");
                    for (int j=0;j<Login.I_list.size(); j++)
                    {
                        if (Login.I_list.get(j).ISBN==item_id){
                            name = Login.I_list.get(j).title;
                            break;
                        }
                    }
                    Object[] row={item_id,name,issueDate,returnDate};
                    model.addRow(row);
                    rs.next();
                }
            }
        }
        catch (Exception e)
        {}
    }
    
    //function to view fine details
    public void fine (int id)
    {
        DefaultTableModel model = (DefaultTableModel) fine_table.getModel();
        int rows = fine_table.getRowCount();
        for (int i=0 ; i<rows ; i++){
            model.removeRow(0);
        }
        
        for(int i=0;i<Login.l_Loan.size();i++)
        {
            if(Login.l_Loan.get(i).PersonId==id)
            {
                int fine = br.fineCalculator(Login.l_Loan.get(i).returnDate);
                int item_id = Login.l_Loan.get(i).ItemIsbn;
                String name="";
                Object issueDate= Login.l_Loan.get(i).issueDate.toLocalDate();
                Object returnDate= Login.l_Loan.get(i).returnDate.toLocalDate();
                for (int j=0;j<Login.I_list.size(); j++)
                {
                    if (Login.I_list.get(j).ISBN==item_id){
                        name = Login.I_list.get(j).title;
                        break;
                    }
                }
                Object[] row={item_id,name,issueDate,returnDate,fine};
                model.addRow(row);
            }
        }
    }
    
    //funtion to add book ISBN in combobox
    public void addISBNOfBook(JComboBox c)
    {
        for(int i=0;i<Login.I_list.size();i++)
        {
            if(Login.I_list.get(i).item_type()== 1)
                c.addItem(Login.I_list.get(i).getISBN());
        }
    }
    
    //funtion to add dvd ISBN in combobox
    public void addISBNOfdvd(JComboBox c)
    {
        for(int i=0;i<Login.I_list.size();i++)
        {
            if(Login.I_list.get(i).item_type()== 0)
                c.addItem(Login.I_list.get(i).getISBN());
        }
    }
    
    //function to remove all values from the combo box
