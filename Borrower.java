    //function to reserve an item
    private void btn_reserveMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_reserveMousePressed
        // TODO add your handling code here:
        if (Login.I_list.isEmpty())
        {
            JOptionPane.showMessageDialog(null, "No item exists to be reserved");
            return ;
        }
        int ISBN = Integer.parseInt(cmb_isbn_reserve.getSelectedItem().toString());
        int user_ID = br.getId(username);
        if (br.checkReservation(ISBN,user_ID))
        {
            br.addReservation(br.maxReserveID(),ISBN,user_ID);
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Reservation for this item has already been made");
        }
    }//GEN-LAST:event_btn_reserveMousePressed

