    //function to renew an item
    private void jButton3MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton3MousePressed
        // TODO add your handling code here:
        if (Login.l_Loan.isEmpty())
        {
            JOptionPane.showMessageDialog(null, "No item has been borrowed yet");
            return ;
        }
        int borrowID = checkForUSerID();
        int reserveID = checkForReserveBook();
        int bookID = Integer.parseInt(cmb_isbn_renew.getSelectedItem().toString());
        LocalDateTime issueDate=LocalDateTime.now();
        LocalDateTime returnDate=LocalDateTime.now().plusDays(12);
        int index=0;
        if (reserveID!=borrowID && reserveID!=-1)
        {
            JOptionPane.showMessageDialog(null, "The item can not be renewed.\nSomeone has already reserved it");
        }
        else
        {
            for(int i=0;i<Login.l_Loan.size();i++)
            {
                if(Login.l_Loan.get(i).ItemIsbn==Integer.parseInt(cmb_isbn_renew.getSelectedItem().toString()))
                {
                    index=i;
                    break;
                }
            }
            Period p = Period.between(Login.l_Loan.get(index).returnDate.toLocalDate(),LocalDateTime.now().toLocalDate());
            int days=p.getDays();
            if(days>=1)
            {
                JOptionPane.showMessageDialog(null, "Fine to be paid is " + calculateFine(days));
            }
            if (reserveID==-1){
                this.lb.deleteReservation(bookID, borrowID);
                for(int i=0;i<Login.l_reseve.size();i++)
                {
                    if(Login.l_reseve.get(i).itemid==bookID && Login.l_reseve.get(i).userid==borrowID)
                    {
                        Login.l_reseve.remove(i);
                        break;
                    }
                }
            }
            this.lb.addCheckIn(bookID, borrowID);
            this.lb.addCheckoutItem(bookID, borrowID, issueDate, returnDate);
        }
    }//GEN-LAST:event_jButton3MousePressed

    //function to view the fine of a user
    private void btn_borrower_fineMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_borrower_fineMousePressed
        // TODO add your handling code here:
        int id = Integer.parseInt(cmb_bxBorrowerFine.getSelectedItem().toString());
        int counter = 0;
        
        DefaultTableModel model = (DefaultTableModel) borrower_fine_table.getModel();
        int rows = borrower_fine_table.getRowCount();
        for (int i=0 ; i<rows ; i++){
            model.removeRow(0);
        }
        
        for(int i=0;i<Login.l_Loan.size();i++)
        {
            if(Login.l_Loan.get(i).PersonId==id)
            {
                counter++;
                int fine = lb.fineCalculator(Login.l_Loan.get(i).returnDate);
                int item_id = Login.l_Loan.get(i).ItemIsbn;
                String name="";
                Object issueDate= Login.l_Loan.get(i).issueDate.toLocalDate();
                Object returnDate= Login.l_Loan.get(i).returnDate.toLocalDate();
                for (int j=0;j<Login.I_list.size(); j++)
                {
                    if (Login.I_list.get(j).ISBN==item_id){
                        name = Login.I_list.get(j).title;
                        break;
                    }
                }
                Object[] row={item_id,name,issueDate,returnDate,fine};
                model.addRow(row);
            }
        }
        
        if (counter==0)
            JOptionPane.showMessageDialog(null, "No items are currently borrowed against this ID");
    }//GEN-LAST:event_btn_borrower_fineMousePressed

