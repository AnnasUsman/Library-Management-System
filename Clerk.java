    private void btn_renewMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_renewMousePressed
        // TODO add your handling code here:
        if (Login.l_Loan.isEmpty())
        {
            JOptionPane.showMessageDialog(null, "No item has been borrowed yet");
            return ;
        }
        int borrowID = checkForUSerID();
        int reserveID = checkForReserveBook();
        int bookID = Integer.parseInt(cmb_renew.getSelectedItem().toString());
        LocalDateTime issueDate=LocalDateTime.now();
        LocalDateTime returnDate=LocalDateTime.now().plusDays(12);
        int index=0;
        if (reserveID!=borrowID && reserveID!=-1)
        {
            JOptionPane.showMessageDialog(null, "The item can not be renewed.\nSomeone has already reserved it");
        }
        else
        {
            for(int i=0;i<Login.l_Loan.size();i++)
            {
                if(Login.l_Loan.get(i).ItemIsbn==Integer.parseInt(cmb_renew.getSelectedItem().toString()))
                {
                    index=i;
                    break;
                }
            }
            Period p = Period.between(Login.l_Loan.get(index).returnDate.toLocalDate(),LocalDateTime.now().toLocalDate());
            int days=p.getDays();
            if(days>=1)
            {
                JOptionPane.showMessageDialog(null, "Fine to be paid is " + calculateFine(days));
            }
            if (reserveID==-1){
                this.cl.deleteReservation(bookID, borrowID);
                for(int i=0;i<Login.l_reseve.size();i++)
                {
                    if(Login.l_reseve.get(i).itemid==bookID && Login.l_reseve.get(i).userid==borrowID)
                    {
                        Login.l_reseve.remove(i);
                        break;
                    }
                }
            }
            this.cl.addCheckIn(bookID, borrowID);
            this.cl.addCheckoutItem(bookID, borrowID, issueDate, returnDate);
        }
    }//GEN-LAST:event_btn_renewMousePressed

