    private void btn_checkoutMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_checkoutMousePressed
        // TODO add your handling code here:
        if (Login.I_list.isEmpty())
        {
            JOptionPane.showMessageDialog(null, "No item exists to be checked-out");
            return ;
        }
        if (cmb_checkout_user.getItemCount()==0)
        {
            JOptionPane.showMessageDialog(null, "Please select an item");
            return ;
        }
        if(AlreadyItemExist()==1)
        {
                DateTimeFormatter dtf = DateTimeFormatter.ofPattern("yyyy-MM-dd HH:mm:ss");  
                if(Login.getNumberOfBorrowedBooks(cmb_checkout_user.getSelectedItem().toString())<5)  //to determing max book limit
                {
                    LocalDateTime issueDate=LocalDateTime.now();
                    LocalDateTime returnDate=LocalDateTime.now().plusDays(12);
                    int user=Integer.parseInt(cmb_checkout_user.getSelectedItem().toString());
                    int item=Integer.parseInt(cmb_checkout_item.getSelectedItem().toString());
                    this.cl.addCheckoutItem(item,user,issueDate,returnDate);
                    Login.l_Loan.add(new Loan(user,item));
                    for(int i=0;i<Login.l_reseve.size();i++)
                    {
                        if(Login.l_reseve.get(i).itemid==item && Login.l_reseve.get(i).userid==user)
                        {
                            Login.l_reseve.remove(i);
                            break;
                        }
                    }
                    cl.deleteReservation(item,user);
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "The borrower has reached the borrow limit of 5");
                }
        }
        else
        {
            JOptionPane.showMessageDialog(null, "The item is already borrowed by someone");
        }
        check=0;
        emptyComboBox(cmb_checkout_user);
        check=1;
    }//GEN-LAST:event_btn_checkoutMousePressed

